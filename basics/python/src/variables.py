# ------------------------------------------------------------------------
#     numbers  -   numeric data types in python
# ------------------------------------------------------------------------

a = 0   # assignment/declaration
a += 1  # addition assignment
a -= 2  # subtraction assignment
a *= 3  # multiplication assignment
a /= 4  # division assignment
a %= 4  # modulus assignment
a //= 4 # floor division assignment
a **= 4 # exponentiation assignment
a &= 4  # bitwise and assignment
a |= 4  # bitwise or assignment
a ^= 4  # bitwise not assignment
a >>= 4 # right shift assignment
a >>= 4 # left shift assignment

# multi-assign
a, b, c = 1, 2, 3
d, e, f = 4

# casting
a = 1.5
a = int(a)