# ------------------------------------------------------------------------
#     numbers  -   numeric data types in python
# ------------------------------------------------------------------------

a = -1      # integer 
b = 1.1     # float
c = 1 + 1j  # complex 

# printing
print("integer: %d" % a)
print("float: %f" % b)
print(f"complex: {c}")


# operations
print(1 + 2)    # addition
print(3 - 4)    # subtraction
print(5 * 6)    # multiplication
print(7 / 8)    # divition
print(9 % 10)   # modulus
print(11 ** 12) # exponentiation
print(13 // 13) # floor division

d = abs(-10)    # absolute value
